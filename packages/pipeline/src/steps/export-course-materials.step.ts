function resolveOutputPath(context: PipelineContext, output: string | undefined, name: string, generatedAt: string): string {
  const requestedOutput = output === undefined || output.trim().length === 0
    ? defaultOutputPath(name, generatedAt)
    : ensureZipExtension(toPortablePath(output.trim()));

  const absoluteOutputPath = path.isAbsolute(requestedOutput)
    ? requestedOutput
    : context.storage.toAbsolutePath(requestedOutput);
  const projectRelativePath = context.storage.toProjectRelativePath(absoluteOutputPath);

  if (!isProjectRelativePath(projectRelativePath)) {
    throw new Error("Course materials export output must be inside the project root.");
  }

  return projectRelativePath;
}

function defaultOutputPath(name: string, generatedAt: string): string {
  return joinPortablePath(
    "generated",
    "exports",
    `${toFileSlug(name)}__ai-course-builder__private-local-mirror__${toTimestampSlug(generatedAt)}.zip`
  );
}

function normalizeExportName(name: string | undefined): string {
  const normalizedName = name?.trim();
  return normalizedName === undefined || normalizedName.length === 0 ? defaultExportName : normalizedName;
}

function ensureZipExtension(filePath: string): string {
  return filePath.toLowerCase().endsWith(".zip") ? filePath : `${filePath}.zip`;
}

function toFileSlug(value: string): string {
  const slug = value
    .toLowerCase()
    .replace(/[^a-z0-9]+/gu, "-")
    .replace(/^-+|-+$/gu, "");
  return slug.length === 0 ? "codex-course-materials" : slug;
}

function toTimestampSlug(value: string): string {
  return value
    .replace(/[-:]/gu, "")
    .replace(/\.\d{3}Z$/u, "Z");
}
